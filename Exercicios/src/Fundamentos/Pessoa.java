package Fundamentos;

public class Pessoa {

	private String nome;
	private int cpf;
	private int salario;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public int getSalário() {
		return salario;
	}
	public void setSalario(int salário) {
		this.salario = salário;
	}
	

	
}
